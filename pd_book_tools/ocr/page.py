    def merge_lines(self, line_indices: list[int]) -> bool:
        """Merge multiple lines into the first selected line."""
        try:
            lines = list(self.lines)
            line_count_before = len(lines)

            logger.debug(
                "merge_lines start: page_type=%s, line_count=%d, requested=%s",
                type(self).__name__,
                line_count_before,
                line_indices,
            )

            if line_count_before < 2:
                logger.warning("Line merge requires at least two available lines")
                return False

            unique_indices = sorted(set(line_indices or []))
            if len(unique_indices) < 2:
                logger.warning("Line merge requires selecting at least two lines")
                return False

            for index in unique_indices:
                if index < 0 or index >= line_count_before:
                    logger.warning(
                        "Line index %s out of range (0-%s)",
                        index,
                        line_count_before - 1,
                    )
                    return False
                if not isinstance(lines[index], Block):
                    logger.warning(
                        "Selected line is not a Block (index=%s, type=%s)",
                        index,
                        type(lines[index]).__name__,
                    )
                    return False

            primary_index = unique_indices[0]
            primary_line = lines[primary_index]
            for index in unique_indices[1:]:
                secondary_line = lines[index]
                try:
                    primary_line.merge(secondary_line)
                except Exception as merge_error:
                    if not BoundingBox.is_geometry_normalization_error(merge_error):
                        raise
                    logger.warning(
                        "Line merge hit malformed bbox metadata (primary=%s secondary=%s): %s; applying merge fallback",
                        primary_index,
                        index,
                        merge_error,
                    )
                    if not primary_line.merge_fallback(secondary_line):
                        return False

            for index in reversed(unique_indices[1:]):
                self.remove_line_if_exists(lines[index])

            try:
                self.finalize_page_structure()
            except Exception as finalize_error:
                if not BoundingBox.is_geometry_normalization_error(finalize_error):
                    raise
                logger.warning(
                    "Merged lines but skipped final bbox recompute due to malformed geometry: %s",
                    finalize_error,
                )
                self._remove_empty_items_safely()
                self._recompute_paragraph_bboxes()

            lines_after = len(list(self.lines))
            logger.info(
                "Merged %d lines into line %d (line_count %d -> %d)",
                len(unique_indices),
                primary_index,
                line_count_before,
                lines_after,
            )
            return True

        except Exception as e:
            logger.exception("Error merging lines %s: %s", line_indices, e)
            return False

    def delete_lines(self, line_indices: list[int]) -> bool:
        """Delete selected lines from the page."""
        try:
            lines = list(self.lines)
            line_count_before = len(lines)

            logger.debug(
                "delete_lines start: page_type=%s, line_count=%d, requested=%s",
                type(self).__name__,
                line_count_before,
                line_indices,
            )

            unique_indices = sorted(set(line_indices or []))
            if not unique_indices:
                logger.warning("Line deletion requires selecting at least one line")
                return False

            for index in unique_indices:
                if index < 0 or index >= line_count_before:
                    logger.warning(
                        "Line index %s out of range (0-%s)",
                        index,
                        line_count_before - 1,
                    )
                    return False

            for index in reversed(unique_indices):
                self.remove_line_if_exists(lines[index])

            lines_after = len(list(self.lines))
            logger.info(
                "Deleted %d lines (line_count %d -> %d)",
                len(unique_indices),
                line_count_before,
                lines_after,
            )
            return True

        except Exception as e:
            logger.exception("Error deleting lines %s: %s", line_indices, e)
            return False

    def delete_words(self, word_keys: list[tuple[int, int]]) -> bool:
        """Delete selected words from the page."""
        try:
            unique_keys = sorted(set(word_keys or []))
            if not unique_keys:
                logger.warning("Word deletion requires selecting at least one word")
                return False

            validated_by_line: dict[int, list] = {}

            for line_index, word_index in unique_keys:
                line_words = validated_by_line.get(line_index)
                if line_words is None:
                    line_words = self.validated_line_words(line_index)
                    if line_words is None:
                        return False
                    validated_by_line[line_index] = line_words
                if word_index < 0 or word_index >= len(line_words):
                    logger.warning(
                        "Word index %s out of range for line %s (0-%s)",
                        word_index,
                        line_index,
                        len(line_words) - 1,
                    )
                    return False

            keys_by_line: dict[int, list[int]] = {}
            for line_index, word_index in unique_keys:
                keys_by_line.setdefault(line_index, []).append(word_index)

            for line_index, word_indices in keys_by_line.items():
                line_words = self.validated_line_words(line_index)
                if line_words is None:
                    return False
                lines = list(self.lines)
                for word_index in sorted(word_indices, reverse=True):
                    if line_index < 0 or line_index >= len(lines):
                        return False
                    line = lines[line_index]
                    words = list(line_words)
                    if word_index < 0 or word_index >= len(words):
                        return False
                    line.remove_item(words[word_index])

            self.finalize_page_structure()

            logger.info("Deleted %d selected words", len(unique_keys))
            return True

        except Exception as e:
            logger.exception("Error deleting selected words %s: %s", word_keys, e)
            return False

    def split_word(
        self,
        line_index: int,
        word_index: int,
        split_fraction: float,
    ) -> bool:
        """Split a word into two words at a relative horizontal position."""
        if split_fraction <= 0.0 or split_fraction >= 1.0:
            logger.warning(
                "Word split fraction must be between 0 and 1 (exclusive), got %s",
                split_fraction,
            )
            return False

        try:
            lines = list(self.lines)
            if line_index < 0 or line_index >= len(lines):
                logger.warning(
                    "Line index %s out of range (0-%s)",
                    line_index,
                    len(lines) - 1,
                )
                return False

            line = lines[line_index]
            words = list(line.words)
            if word_index < 0 or word_index >= len(words):
                logger.warning(
                    "Word split index %s out of range for line %s (0-%s)",
                    word_index,
                    line_index,
                    len(words) - 1,
                )
                return False

            word = words[word_index]
            word_text = str(word.text or "")
            if len(word_text) < 2:
                logger.warning(
                    "Word split requires at least two characters (line=%s, word=%s)",
                    line_index,
                    word_index,
                )
                return False

            bbox = word.bounding_box
            bbox_width = float(bbox.width if bbox else 0.0)
            if bbox is None or bbox_width <= 0.0:
                logger.warning(
                    "Word split requires valid non-zero bounding box (line=%s, word=%s)",
                    line_index,
                    word_index,
                )
                return False

            character_split_index = round(len(word_text) * split_fraction)
            character_split_index = max(
                1, min(len(word_text) - 1, character_split_index)
            )

            epsilon = min(1e-6, bbox_width / 10) if bbox_width > 0 else 0.0
            bbox_split_offset = bbox_width * split_fraction
            bbox_split_offset = max(
                epsilon, min(bbox_width - epsilon, bbox_split_offset)
            )

            line.split_word(
                split_word_index=word_index,
                bbox_split_offset=bbox_split_offset,
                character_split_index=character_split_index,
            )

            updated_words = self.validated_line_words(line_index)
            if updated_words is None:
                return False
            for updated_word in updated_words:
                updated_word.ground_truth_text = ""

            self.finalize_page_structure()

            logger.info(
                "Split word line=%d index=%d fraction=%.3f char_index=%d",
                line_index,
                word_index,
                split_fraction,
                character_split_index,
            )
            return True

        except Exception as e:
            logger.exception(
                "Error splitting word line=%s index=%s split_fraction=%s: %s",
                line_index,
                word_index,
                split_fraction,
                e,
            )
            return False

    def split_word_vertically_and_assign_to_closest_line(
        self,
        line_index: int,
        word_index: int,
        split_fraction: float,
    ) -> bool:
        """Split a word, then assign each resulting piece to the closest line by y-midpoint."""
        lines_before_split = list(self.lines)
        if line_index < 0 or line_index >= len(lines_before_split):
            logger.warning(
                "Line index %s out of range (0-%s)",
                line_index,
                len(lines_before_split) - 1,
            )
            return False
        source_line = lines_before_split[line_index]

        if not self.split_word(line_index, word_index, split_fraction):
            return False

        try:
            lines = list(self.lines)
            source_words = list(source_line.words)
            if word_index < 0 or word_index + 1 >= len(source_words):
                logger.warning(
                    "Post-split word indices %s/%s unavailable on line %s",
                    word_index,
                    word_index + 1,
                    line_index,
                )
                return False

            split_words = [source_words[word_index], source_words[word_index + 1]]

            touched_lines: dict[int, Block] = {id(source_line): source_line}
            for split_piece in split_words:
                piece_bbox = split_piece.bounding_box
                midpoint_y = (
                    piece_bbox.vertical_midpoint if piece_bbox is not None else None
                )
                target_line = Page.closest_line_by_midpoint(
                    lines,
                    midpoint_y,
                    fallback_line=source_line,
                )
                touched_lines[id(target_line)] = target_line

                if target_line is source_line:
                    continue

                if not Page.move_word_between_lines(
                    source_line, target_line, split_piece
                ):
                    logger.warning(
                        "Failed to move split word piece to closest line (line=%s word=%s)",
                        line_index,
                        word_index,
                    )
                    return False

            for touched_line in touched_lines.values():
                touched_words = list(touched_line.words)
                for touched_word in touched_words:
                    touched_word.ground_truth_text = ""
                touched_line.recompute_bounding_box()

            self.finalize_page_structure()

            logger.info(
                "Split word vertically with closest-line assignment line=%d index=%d fraction=%.3f",
                line_index,
                word_index,
                split_fraction,
            )
            return True
        except Exception as e:
            logger.exception(
                "Error splitting word vertically line=%s index=%s split_fraction=%s: %s",
                line_index,
                word_index,
                split_fraction,
                e,
            )
            return False

    def split_line_after_word(
        self,
        line_index: int,
        word_index: int,
    ) -> bool:
        """Split a line into two lines immediately after the selected word."""
        from pd_book_tools.ocr.block import BlockChildType

        try:
            lines = list(self.lines)
            if line_index < 0 or line_index >= len(lines):
                logger.warning(
                    "Line index %s out of range for line split (0-%s)",
                    line_index,
                    len(lines) - 1,
                )
                return False

            target_line = lines[line_index]
            line_words = list(target_line.words)
            if len(line_words) < 2:
                logger.warning(
                    "Line split requires at least two words (line index %s)",
                    line_index,
                )
                return False

            if word_index < 0 or word_index >= len(line_words) - 1:
                logger.warning(
                    "Cannot split line %s after word index %s (valid range: 0-%s)",
                    line_index,
                    word_index,
                    len(line_words) - 2,
                )
                return False

            first_words = line_words[: word_index + 1]
            second_words = line_words[word_index + 1 :]

            paragraphs = list(self.paragraphs)
            target_paragraph = None
            for paragraph in paragraphs:
                if target_line in list(paragraph.lines):
                    target_paragraph = paragraph
                    break

            if target_paragraph is None:
                logger.warning(
                    "Unable to find paragraph containing line index %s", line_index
                )
                return False

            parent = self.find_parent_block(target_paragraph)
            if parent is None:
                logger.warning(
                    "Unable to locate parent block for line split after word (%s, %s)",
                    line_index,
                    word_index,
                )
                return False

            paragraph_items = list(target_paragraph.items)
            if target_line not in paragraph_items:
                logger.warning(
                    "Target line missing in paragraph items for line split (%s, %s)",
                    line_index,
                    word_index,
                )
                return False

            line_item_index = paragraph_items.index(target_line)
            target_line.items = first_words
            target_line.unmatched_ground_truth_words = []
            target_line.recompute_bounding_box()

            split_line = Block(
                items=second_words,
                child_type=BlockChildType.WORDS,
                block_category=BlockCategory.LINE,
            )

            target_paragraph.items = [
                *paragraph_items[: line_item_index + 1],
                split_line,
                *paragraph_items[line_item_index + 1 :],
            ]
            target_paragraph.recompute_bounding_box()
            parent.recompute_bounding_box()

            self.finalize_page_structure()

            logger.info("Split line %s after word %s", line_index, word_index)
            return True

        except Exception as e:
            logger.exception(
                "Error splitting line after word line=%s word=%s: %s",
                line_index,
                word_index,
                e,
            )
            return False

    def rebox_word(
        self,
        line_index: int,
        word_index: int,
        x1: float,
        y1: float,
        x2: float,
        y2: float,
        refine_after: bool = True,
    ) -> bool:
        """Replace a word bounding box with the provided rectangle."""
        from pd_book_tools.geometry.point import Point

        try:
            line_words = self.validated_line_words(line_index)
            if line_words is None:
                return False
            if word_index < 0 or word_index >= len(line_words):
                logger.warning(
                    "Word rebox index %s out of range for line %s (0-%s)",
                    word_index,
                    line_index,
                    len(line_words) - 1,
                )
                return False

            rx1, ry1 = min(float(x1), float(x2)), min(float(y1), float(y2))
            rx2, ry2 = max(float(x1), float(x2)), max(float(y1), float(y2))
            if rx2 <= rx1 or ry2 <= ry1:
                logger.warning(
                    "Invalid rebox rectangle for line=%s word=%s: (%s, %s, %s, %s)",
                    line_index,
                    word_index,
                    rx1,
                    ry1,
                    rx2,
                    ry2,
                )
                return False

            word = line_words[word_index]
            existing_bbox = word.bounding_box
            is_normalized = bool(
                existing_bbox.is_normalized if existing_bbox else False
            )

            if is_normalized:
                page_width, page_height = self.resolved_dimensions
                if page_width <= 0.0 or page_height <= 0.0:
                    logger.warning(
                        "Unable to resolve page dimensions for normalized rebox"
                    )
                    return False
                new_bbox = BoundingBox(
                    Point(rx1 / page_width, ry1 / page_height),
                    Point(rx2 / page_width, ry2 / page_height),
                    is_normalized=True,
                )
            else:
                new_bbox = BoundingBox(
                    Point(rx1, ry1),
                    Point(rx2, ry2),
                    is_normalized=False,
                )

            word.bounding_box = new_bbox
            if refine_after:
                word.refine_bbox(self.cv2_numpy_page_image)
            self.finalize_page_structure()

            logger.info(
                "Reboxed word line=%d index=%d bbox=(%.2f, %.2f, %.2f, %.2f)",
                line_index,
                word_index,
                rx1,
                ry1,
                rx2,
                ry2,
            )
            return True
        except Exception as e:
            logger.exception(
                "Error reboxing word line=%s index=%s: %s",
                line_index,
                word_index,
                e,
            )
            return False

    def add_word_to_page(
        self,
        x1: float,
        y1: float,
        x2: float,
        y2: float,
        text: str = "",
    ) -> bool:
        """Insert a new word with the given bounding box into the nearest line."""
        from pd_book_tools.geometry.point import Point

        try:
            rx1, ry1 = min(float(x1), float(x2)), min(float(y1), float(y2))
            rx2, ry2 = max(float(x1), float(x2)), max(float(y1), float(y2))
            if rx2 <= rx1 or ry2 <= ry1:
                logger.warning(
                    "Invalid add-word rectangle: (%.2f, %.2f, %.2f, %.2f)",
                    rx1,
                    ry1,
                    rx2,
                    ry2,
                )
                return False

            lines = list(self.lines)
            if not lines:
                logger.warning("No lines found in page for add_word_to_page")
                return False

            is_normalized = self.is_content_normalized
            page_width, page_height = 0.0, 0.0

            if is_normalized:
                page_width, page_height = self.resolved_dimensions
                if page_width <= 0.0 or page_height <= 0.0:
                    logger.warning("Unable to resolve page dimensions for add_word")
                    return False
                new_bbox = BoundingBox(
                    Point(rx1 / page_width, ry1 / page_height),
                    Point(rx2 / page_width, ry2 / page_height),
                    is_normalized=True,
                )
            else:
                new_bbox = BoundingBox(
                    Point(rx1, ry1),
                    Point(rx2, ry2),
                    is_normalized=False,
                )

            new_word = Word(
                text=text,
                bounding_box=new_bbox,
                ocr_confidence=None,
            )

            cx = (rx1 + rx2) / 2.0
            cy = (ry1 + ry2) / 2.0
            if is_normalized:
                cx = cx / page_width
                cy = cy / page_height

            target_line = Page.closest_line_by_y_range_then_x(
                lines,
                cx,
                cy,
                lines[0],
            )

            target_line.add_item(new_word)

            self.finalize_page_structure()

            logger.info(
                "Added new word to page bbox=(%.2f, %.2f, %.2f, %.2f)",
                rx1,
                ry1,
                rx2,
                ry2,
            )
            return True

        except Exception as e:
            logger.exception("Error adding word to page: %s", e)
            return False

    def split_line_with_selected_words(
        self,
        word_keys: list[tuple[int, int]],
    ) -> bool:
        """Move selected words into exactly one newly created line."""
        from pd_book_tools.ocr.block import BlockChildType

        unique_keys = sorted(set(word_keys or []))
        if not unique_keys:
            logger.warning(
                "Split-line-by-selected-words requires at least one selected word"
            )
            return False

        try:
            lines = list(self.lines)

            line_to_selected_word_indices: dict[int, set[int]] = {}
            for line_index, word_index in unique_keys:
                if line_index < 0 or line_index >= len(lines):
                    logger.warning(
                        "Word key (%s, %s) line index out of range (0-%s)",
                        line_index,
                        word_index,
                        len(lines) - 1,
                    )
                    return False
                line_to_selected_word_indices.setdefault(line_index, set()).add(
                    word_index
                )

            selected_words_for_new_line: list[Word] = []
            source_line_bboxes: list = []
            containing_paragraphs: list[Block] = []
            line_insertion_points: list[tuple[Block, int]] = []
            emptied_line_ids: set[int] = set()
            for line_index in sorted(line_to_selected_word_indices):
                selected_word_indices = line_to_selected_word_indices[line_index]
                target_line = lines[line_index]
                line_words = list(target_line.words)
                source_line_original_bbox = target_line.bounding_box

                if len(line_words) < 1:
                    logger.warning("Line %s has no words", line_index)
                    return False

                for wi in selected_word_indices:
                    if wi < 0 or wi >= len(line_words):
                        logger.warning(
                            "Word index %s out of range for line %s (0-%s)",
                            wi,
                            line_index,
                            len(line_words) - 1,
                        )
                        return False

                selected_words = [
                    line_words[wi]
                    for wi in range(len(line_words))
                    if wi in selected_word_indices
                ]
                unselected_words = [
                    line_words[wi]
                    for wi in range(len(line_words))
                    if wi not in selected_word_indices
                ]

                if not selected_words:
                    logger.warning("No selected words found on line %s", line_index)
                    return False

                selected_words_for_new_line.extend(selected_words)
                if (
                    source_line_original_bbox is not None
                    and source_line_original_bbox.has_usable_coordinates
                ):
                    source_line_bboxes.append(source_line_original_bbox)

                paragraphs = list(self.paragraphs)
                target_paragraph = None
                for paragraph in paragraphs:
                    if target_line in list(paragraph.lines):
                        target_paragraph = paragraph
                        break

                if target_paragraph is None:
                    logger.warning(
                        "Unable to find paragraph containing line %s", line_index
                    )
                    return False
                containing_paragraphs.append(target_paragraph)

                paragraph_items = list(target_paragraph.items)
                if target_line not in paragraph_items:
                    logger.warning(
                        "Target line missing in paragraph items for line %s",
                        line_index,
                    )
                    return False

                line_item_index = paragraph_items.index(target_line)
                line_insertion_points.append((target_paragraph, line_item_index))

                target_line.items = unselected_words
                target_line.unmatched_ground_truth_words = []
                if not unselected_words:
                    emptied_line_ids.add(id(target_line))

                if unselected_words:
                    try:
                        target_line.recompute_bounding_box()
                    except Exception as recompute_error:
                        if not BoundingBox.is_geometry_normalization_error(
                            recompute_error
                        ):
                            raise
                        logger.warning(
                            "Skipped source line bbox recompute due to malformed geometry on line %s: %s",
                            line_index,
                            recompute_error,
                        )

                if (
                    unselected_words
                    and not (
                        target_line.bounding_box is not None
                        and target_line.bounding_box.has_usable_coordinates
                    )
                    and (
                        source_line_original_bbox is not None
                        and source_line_original_bbox.has_usable_coordinates
                    )
                ):
                    target_line.bounding_box = source_line_original_bbox

            if not selected_words_for_new_line:
                logger.warning("No selected words found for single-line extraction")
                return False

            new_line = Block(
                items=selected_words_for_new_line,
                bounding_box=Page.first_usable_bbox(source_line_bboxes),
                child_type=BlockChildType.WORDS,
                block_category=BlockCategory.LINE,
            )
            try:
                new_line.recompute_bounding_box()
            except Exception as recompute_error:
                if not BoundingBox.is_geometry_normalization_error(recompute_error):
                    raise
                logger.warning(
                    "Skipped extracted line bbox recompute due to malformed geometry: %s",
                    recompute_error,
                )

            if (
                not (
                    new_line.bounding_box is not None
                    and new_line.bounding_box.has_usable_coordinates
                )
                and source_line_bboxes
            ):
                new_line.bounding_box = Page.first_usable_bbox(source_line_bboxes)

            unique_paragraphs = []
            for paragraph in containing_paragraphs:
                if paragraph not in unique_paragraphs:
                    unique_paragraphs.append(paragraph)

            if len(unique_paragraphs) == 1 and line_insertion_points:
                target_paragraph = unique_paragraphs[0]
                original_paragraph_items = list(target_paragraph.items)
                paragraph_items = [
                    item
                    for item in original_paragraph_items
                    if id(item) not in emptied_line_ids
                ]
                insert_after = max(
                    item_index
                    for paragraph, item_index in line_insertion_points
                    if paragraph is target_paragraph
                )
                insert_at = sum(
                    1
                    for idx, item in enumerate(original_paragraph_items)
                    if idx <= insert_after and id(item) not in emptied_line_ids
                )
                target_paragraph.items = [
                    *paragraph_items[:insert_at],
                    new_line,
                    *paragraph_items[insert_at:],
                ]
            else:
                new_paragraph = Block(
                    items=[new_line],
                    bounding_box=Page.first_usable_bbox(
                        source_line_bboxes
                        + [paragraph.bounding_box for paragraph in unique_paragraphs]
                    ),
                    child_type=BlockChildType.BLOCKS,
                    block_category=BlockCategory.PARAGRAPH,
                )
                self.items = [*list(self.items), new_paragraph]

            self.finalize_page_structure()
            logger.info(
                "Moved selected words into one new line from %d source line(s)",
                len(line_to_selected_word_indices),
            )
            return True
        except Exception as e:
            logger.exception(
                "Error splitting lines by selected words %s: %s",
                unique_keys,
                e,
            )
            return False

    def split_lines_into_selected_and_unselected_words(
        self,
        word_keys: list[tuple[int, int]],
    ) -> bool:
        """Split each affected line into selected-word and unselected-word lines."""
        from pd_book_tools.ocr.block import BlockChildType

        unique_keys = sorted(set(word_keys or []))
        if not unique_keys:
            logger.warning(
                "Split-lines-into-selected-unselected requires at least one selected word"
            )
            return False

        try:
            lines = list(self.lines)

            line_to_selected_word_indices: dict[int, set[int]] = {}
            for line_index, word_index in unique_keys:
                if line_index < 0 or line_index >= len(lines):
                    logger.warning(
                        "Word key (%s, %s) line index out of range (0-%s)",
                        line_index,
                        word_index,
                        len(lines) - 1,
                    )
                    return False
                line_to_selected_word_indices.setdefault(line_index, set()).add(
                    word_index
                )

            split_any = False
            for line_index in sorted(line_to_selected_word_indices, reverse=True):
                selected_word_indices = line_to_selected_word_indices[line_index]
                target_line = lines[line_index]
                line_words = list(target_line.words)

                if len(line_words) < 2:
                    logger.warning(
                        "Line %s has fewer than 2 words; cannot split by selection",
                        line_index,
                    )
                    continue

                for wi in selected_word_indices:
                    if wi < 0 or wi >= len(line_words):
                        logger.warning(
                            "Word index %s out of range for line %s (0-%s)",
                            wi,
                            line_index,
                            len(line_words) - 1,
                        )
                        return False

                selected_words = [
                    line_words[wi]
                    for wi in range(len(line_words))
                    if wi in selected_word_indices
                ]
                unselected_words = [
                    line_words[wi]
                    for wi in range(len(line_words))
                    if wi not in selected_word_indices
                ]

                if not selected_words or not unselected_words:
                    logger.warning(
                        "Split-by-selection requires selected and unselected words on line %s",
                        line_index,
                    )
                    continue

                paragraphs = list(self.paragraphs)
                target_paragraph = None
                for paragraph in paragraphs:
                    if target_line in list(paragraph.lines):
                        target_paragraph = paragraph
                        break

                if target_paragraph is None:
                    logger.warning(
                        "Unable to find paragraph containing line %s", line_index
                    )
                    return False

                paragraph_items = list(target_paragraph.items)
                if target_line not in paragraph_items:
                    logger.warning(
                        "Target line missing in paragraph items for line %s",
                        line_index,
                    )
                    return False

                line_item_index = paragraph_items.index(target_line)

                target_line.items = selected_words
                target_line.unmatched_ground_truth_words = []
                target_line.recompute_bounding_box()

                new_line = Block(
                    items=unselected_words,
                    child_type=BlockChildType.WORDS,
                    block_category=BlockCategory.LINE,
                )

                target_paragraph.items = [
                    *paragraph_items[: line_item_index + 1],
                    new_line,
                    *paragraph_items[line_item_index + 1 :],
                ]
                target_paragraph.recompute_bounding_box()

                parent = self.find_parent_block(target_paragraph)
                if parent is not None:
                    parent.recompute_bounding_box()

                split_any = True

            if not split_any:
                logger.warning("No lines were split into selected/unselected groups")
                return False

            self.finalize_page_structure()
            logger.info(
                "Split %d line(s) into selected/unselected words",
                len(line_to_selected_word_indices),
            )
            return True
        except Exception as e:
            logger.exception(
                "Error splitting lines into selected/unselected words %s: %s",
                unique_keys,
                e,
            )
            return False

    def nudge_word_bbox(
        self,
        line_index: int,
        word_index: int,
        left_delta: float,
        right_delta: float,
        top_delta: float,
        bottom_delta: float,
        refine_after: bool = True,
    ) -> bool:
        """Expand/contract a word bounding box by pixel deltas."""
        try:
            line_words = self.validated_line_words(line_index)
            if line_words is None:
                return False
            if word_index < 0 or word_index >= len(line_words):
                logger.warning(
                    "Word nudge index %s out of range for line %s (0-%s)",
                    word_index,
                    line_index,
                    len(line_words) - 1,
                )
                return False

            word = line_words[word_index]
            bbox = word.bounding_box
            if bbox is None:
                logger.warning(
                    "Word bbox nudge requires an existing bbox for line=%s word=%s",
                    line_index,
                    word_index,
                )
                return False

            is_normalized = bool(bbox.is_normalized)
            if is_normalized:
                page_width, page_height = self.resolved_dimensions
                if page_width <= 0.0 or page_height <= 0.0:
                    logger.warning(
                        "Unable to resolve page dimensions for normalized bbox nudge"
                    )
                    return False
                x1 = float(bbox.minX or 0.0) * page_width
                y1 = float(bbox.minY or 0.0) * page_height
                x2 = float(bbox.maxX or 0.0) * page_width
                y2 = float(bbox.maxY or 0.0) * page_height
            else:
                x1 = float(bbox.minX or 0.0)
                y1 = float(bbox.minY or 0.0)
                x2 = float(bbox.maxX or 0.0)
                y2 = float(bbox.maxY or 0.0)

            nx1 = x1 - float(left_delta)
            ny1 = y1 - float(top_delta)
            nx2 = x2 + float(right_delta)
            ny2 = y2 + float(bottom_delta)

            page_width, page_height = self.resolved_dimensions
            nx1 = max(0.0, nx1)
            ny1 = max(0.0, ny1)
            if page_width > 0.0:
                nx2 = min(nx2, page_width)
            if page_height > 0.0:
                ny2 = min(ny2, page_height)

            if nx2 <= nx1 or ny2 <= ny1:
                logger.warning(
                    "Invalid bbox size after resize for line=%s word=%s: (%s, %s, %s, %s)",
                    line_index,
                    word_index,
                    nx1,
                    ny1,
                    nx2,
                    ny2,
                )
                return False

            return self.rebox_word(
                line_index,
                word_index,
                nx1,
                ny1,
                nx2,
                ny2,
                refine_after=refine_after,
            )
        except Exception as e:
            logger.exception(
                "Error resizing word bbox line=%s index=%s deltas=(l=%s r=%s t=%s b=%s): %s",
                line_index,
                word_index,
                left_delta,
                right_delta,
                top_delta,
                bottom_delta,
                e,
            )
            raise

    def scale(self, width: int, height: int) -> Page:
        """
        Return new page with scaled bounding box
        to absolute pixel coordinates
        """
        return Page(
            width=width,
            height=height,
            page_index=self.page_index,
            blocks=[item.scale(width, height) for item in self.items],
            bounding_box=self.bounding_box.scale(width, height)
            if self.bounding_box
            else None,
            page_labels=self.page_labels,
            ocr_provenance=self.ocr_provenance,
        )

    def to_dict(self) -> dict[str, Any]:
        """Convert to JSON-serializable dictionary"""
        result: dict[str, Any] = {
            "type": "Page",
            "width": self.width,
            "height": self.height,
            "page_index": self.page_index,
            "bounding_box": self.bounding_box.to_dict() if self.bounding_box else None,
            "items": [item.to_dict() for item in self.items] if self.items else [],
            "ocr_provenance": (
                self.ocr_provenance.to_dict()  # type: ignore[reportAttributeAccessIssue]  # declared as union for deserialization; coerce() normalizes to OCRProvenance|None at init
                if self.ocr_provenance is not None
                else None
            ),
        }
        # Include metadata fields when set (omit defaults for compact output)
        if self.image_path is not None:
            result["image_path"] = str(self.image_path)
        if self.name is not None:
            result["name"] = self.name
        if self.source != "ocr":
            result["source"] = self.source
        if self.ocr_failed:
            result["ocr_failed"] = self.ocr_failed
        if self.provenance_live_ocr is not None:
            result["provenance_live_ocr"] = self.provenance_live_ocr
        if self.provenance_saved_ocr is not None:
            result["provenance_saved_ocr"] = self.provenance_saved_ocr
        if self.provenance_saved is not None:
            result["provenance_saved"] = self.provenance_saved
        if self.rotation_applied:
            result["rotation_applied"] = self.rotation_applied
        if self.review is not None:
            result["review"] = self.review.to_dict()
        return result

    def copy(self) -> Page:
        # Copy the page to a new object via serialization/deserialization
        return self.from_dict(self.to_dict())

    def reorganize_page(
        self,
        layout: PageLayout | None = None,
        *,
        drop_figure_internal_text: bool = True,
        drop_layout_words: bool = False,
        capture_diagnostics: bool = True,
        emit_illustration_placeholders: bool = True,
        sidenote_max_height_ratio: float | None = None,
    ):
        """Top-level reorganize pipeline — thin orchestration shim.

        All pipeline-step logic lives in
        :mod:`pd_book_tools.ocr.reorganize_page_utils`. See
        ``docs/specs/03-reorganize-pipeline.md`` for the per-step
        heuristics, debug-PNG outputs, and the rationale behind each
        threshold.

        When ``layout`` is supplied, layout-derived hints feed two extra
        passes — see :mod:`pd_book_tools.ocr.layout_aware_reorg` for the
        helpers and rationale. Layout is treated as a hint (low-confidence
        regions are ignored); the existing geometric heuristics run as a
        safety net regardless.

        ``drop_figure_internal_text`` toggles the heuristic figure-noise
        sweep at Step B2. Default is on because PGDP-style ground truth
        rarely transcribes characters that fall inside an illustration
        (engraving textures, halftone speckle, map-interior fragments),
        so dropping them aligns the OCR output with the human-edited
        reference. Pass ``False`` to keep every OCR token — useful when
        the consumer needs the raw OCR (e.g. training data prep where a
        glyph inside a figure is still a labelled glyph).

        ``drop_layout_words`` is an experimental opt-in that gates
        **all** word-deletion paths in the pipeline. Two passes are
        controlled by it:

        * Step Layout-2b
          (:func:`layout_aware_reorg.drop_figure_internal_words`) —
          drops OCR lines whose words are all purely tagged
          ``layout:figure`` (no overlapping ``layout:text``).
        * Step B2
          (:func:`reorganize_page_utils.drop_heuristic_figure_noise`) —
          geometric figure-noise sweep that runs whether or not a
          ``layout`` was supplied. Additionally requires
          ``drop_figure_internal_text=True`` (the legacy knob) to
          actually fire.

        With the default (``drop_layout_words=False``), **no** OCR
        words are removed by the pipeline — neither figure-internal-only
        words (Step Layout-2b) nor figure heuristic noise (Step B2).
        ``validate_word_preservation`` then holds end-to-end: every
        pre-pipeline OCR word is present in the post-pipeline output.
        Footnote / header / footer / abandoned regions are also NEVER
        dropped, regardless of this flag — only their per-word
        ``layout:*`` tags and (where mapped) the bubbled
        ``block_role_labels`` from
        :func:`bubble_block_roles_from_layout` are added so downstream
        consumers can dispatch on them. ``pre_reorg_words`` is still
        captured *after* the (now-gated) Step Layout-2b / Step B2
        capture point by design, so the post-reorg reconciler sees an
        unchanged frame of reference.

        ``drop_figure_internal_text`` keeps its independent meaning as
        a sub-gate on the Step B2 heuristic sweep — it has effect only
        when ``drop_layout_words=True`` (Step Layout-2b is unaffected
        by it).

        ``sidenote_max_height_ratio`` threads through to Step Layout-1b
        (:func:`layout_aware_reorg.detect_geometric_sidenotes`) as its
        ``max_height_ratio`` glyph-size gate. Default ``None`` preserves
        legacy x-only sidenote detection. Pass e.g. ``0.85`` to require
        a candidate margin cluster's median bbox height to be at most
        85 % of the body median before tagging — useful when the
        geometric x-cluster catches a same-font full-line that happens
        to live in the margin (chapter rules, page numbers, decorative
        flourishes). Has effect only when ``layout`` is supplied (Step
        Layout-1b only fires in the layout-aware branch).

        Diagnostic snapshots
        --------------------
        When ``capture_diagnostics`` is true (default), two deep-copy
        snapshots of ``self`` are stashed for CLI / debugging
        consumption:

        * ``self.diagnostic_pure_ocr`` — captured at the very top of
          this method, before any layout tagging or noise removal.
          Reflects the literal OCR engine output as it entered the
          pipeline. No ``layout:*`` word labels, no figure-noise drops.

        * ``self.diagnostic_post_noise_removal`` — captured at the
          same point as the internal ``pre_reorg_words`` list used by
          :func:`reorganize_page_utils.reconcile_dropped_words`. That
          point is *after* Step Layout-2b
          (:func:`layout_aware_reorg.drop_figure_internal_words`) and
          Step B2 (:func:`reorganize_page_utils.drop_heuristic_figure_noise`)
          have had their chance to run, but *before* any reorg-proper
          step (line merging, header/footer extraction, row blocks,
          paragraphizing). Reading-order metadata has not been
          stamped yet.

        Each snapshot is a full ``Page`` clone (via
        :meth:`Page.copy`) so the existing exporters (``snapshot.text``,
        ``snapshot.to_dict()``, ``snapshot.lines``, ``snapshot.words``,
        ...) work on it identically to the live page. Snapshots are
        diagnostic-only: they are NOT used by any production logic
        and are NOT persisted by ``to_dict`` / ``copy``. Pass
        ``capture_diagnostics=False`` to skip the deep-copy work in
        latency-sensitive paths.

        In addition to the two ``Page`` snapshots, two scalar
        diagnostics are *always* populated (regardless of
        ``capture_diagnostics``) since they're cheap and the CLI uses
        them as the trigger for its "likely noise" warning:

        * ``self.diagnostic_noise_dropped_words`` — list of deep-copied
          ``Word`` objects that were removed by Step Layout-2b
          (figure-internal words) and / or Step B2 (heuristic figure
          noise). Each entry preserves the OCR engine's text + bbox
          + confidence + word_labels, so the CLI can format an
          informative warning. Empty list when no noise removal fired.

        * ``self.diagnostic_noise_dropped_count`` — convenience int
          equal to ``len(self.diagnostic_noise_dropped_words)``. The
          CLI emits a stderr warning whenever this is > 0, regardless
          of whether export-pre-reorg-JSON was requested.
        """
        logger.debug("Reorganizing Page")

        # Reset the diagnostic noise-drop accumulators so a second
        # reorganize call doesn't accumulate dropped words from a
        # previous pass.
        self.diagnostic_noise_dropped_words = []
        self.diagnostic_noise_dropped_count = 0

        # Snapshot 1 of 2: pure OCR — the page as the OCR engine produced
        # it, before *any* mutation by this pipeline. Captured up front
        # so even an exception later in the pipeline doesn't deny callers
        # access to the unmodified input. Cheap enough at typical page
        # sizes that we always capture by default; ``capture_diagnostics``
        # is the explicit opt-out.
        if capture_diagnostics:
            self.diagnostic_pure_ocr = self.copy()

        # Capture references to the live ``Word`` objects right at the
        # top, before Step Layout-2b can drop any of them. Tracked by
        # ``id()`` so the noise-drop diff is robust to ``refine_bounding_boxes``
        # tightening bbox coordinates between the two snapshots — the
        # underlying ``Word`` instance flows through unchanged.
        live_pre_noise_words = list(self.words)

        if layout is not None:
            from pd_book_tools.ocr import layout_aware_reorg

            # Folded into the pipeline as Step Layout-1a: tag every word with
            # the region(s) it sits in. Downstream steps (and post-reorg
            # consumers) see these as ``layout:<type>`` entries in
            # ``word.word_labels``.
            layout_aware_reorg.tag_words_with_layout(self, layout)

            # Step Layout-1b: geometric fallback — tag clear left/right
            # margin-column words as ``layout:sidenote``. PP-DocLayout
            # often lumps narrow marginalia into the body's wide text
            # region; this catches what the model misses.
            layout_aware_reorg.detect_geometric_sidenotes(
                self, max_height_ratio=sidenote_max_height_ratio
            )

            # Step Layout-2a (formerly: drop high-confidence header /
            # footer / footnote / abandoned regions before the geometric
            # pipeline runs). Removed from the default pipeline: those
            # regions are real content (footnote text, page numbers,
            # marginalia tagged ``abandoned``) and must survive the
            # reorg. The per-word ``layout:<type>`` tags are still
            # attached by ``tag_words_with_layout`` above, and (where
            # mapped) ``bubble_block_roles_from_layout`` emits the
            # corresponding ``block_role_labels`` later in the pipeline,
            # so downstream consumers can still dispatch on
            # header / footer / footnote without us silently deleting
            # words. ``drop_layout_regions`` itself is unchanged and
            # still importable for callers that want to opt in
            # explicitly with their own ``drop_types`` set.

            # Step Layout-2b: drop OCR lines that are entirely figure-internal
            # noise — only when *every* word in the line is purely tagged
            # ``layout:figure`` with no other layout tag. Body text that
            # wraps around a figure is preserved because PP-DocLayout emits
            # a separate text region for the wrap zone, so wrap words also
            # carry ``layout:text``.
            #
            # Gated behind the experimental ``drop_layout_words`` opt-in.
            # Under the default (``drop_layout_words=False``) the pipeline
            # never silently deletes OCR words: even a line whose words
            # are all tagged ``layout:figure`` is preserved. The per-word
            # ``layout:figure`` tags remain attached by
            # ``tag_words_with_layout`` above, so downstream consumers
            # that *want* to filter figure-internal words can still
            # dispatch on the tags without the library having decided
            # for them.
            if drop_layout_words:
                layout_aware_reorg.drop_figure_internal_words(self, layout)

        # Step A — image-based bounding box tightening.
        if self._cv2_numpy_page_image is not None:
            self.refine_bounding_boxes()

        debug_sections: list[tuple[str, list[str]]] = []
        debug_squeezed_lines: list[str] = []

        # Step B — merge OCR-fragmented lines back together.
        for block in self.items:
            reorganize_page_utils.reorganize_lines(block)

        # Step B2 — heuristic figure-noise drop. Pure-geometry fallback for
        # unmodelled figure interiors (or noise the model missed); runs
        # whether or not a layout was supplied. Sequenced AFTER
        # reorganize_lines so OCR fragments destined to be merged into
        # longer lines aren't mistaken for solo weak noise. Snapshotting
        # pre_reorg_words AFTER this step makes its drops intentional
        # rather than something strict-mode reconciliation flags.
        # Gated on BOTH ``drop_figure_internal_text`` (legacy knob) AND
        # the experimental ``drop_layout_words`` opt-in — figure-internal
        # heuristic noise is the only word-deletion path the experimental
        # flag still controls. Under the default the pipeline does not
        # silently delete OCR words from this geometric fallback.
        if drop_figure_internal_text and drop_layout_words:
            reorganize_page_utils.drop_heuristic_figure_noise(self)

        # Snapshot the pre-pipeline word set so the post-pipeline reconciler
        # can detect drops and either recover them or hard-fail (strict mode).
        # Captured AFTER refine_bounding_boxes + Step B + heuristic noise
        # filter so the snapshot reflects what the rest of the pipeline is
        # supposed to preserve through Step D onwards.
        pre_reorg_words = list(self.words)

        # Snapshot 2 of 2: post-noise-removal — same capture point as the
        # internal ``pre_reorg_words`` reference list above. Step Layout-2b
        # (``drop_figure_internal_words``) and Step B2
        # (``drop_heuristic_figure_noise``) have already had their chance
        # to run; the rest of the reorg (line merging, header/footer
        # extraction, row blocks, paragraphizing) has not. Captured as a
        # full ``Page`` clone (not a list of word references) so
        # downstream rendering / serialization sees the post-noise state
        # frozen — the live ``self.words`` are about to be re-stitched
        # into the reorganized block tree, which would otherwise mutate
        # any held references.
        if capture_diagnostics:
            self.diagnostic_post_noise_removal = self.copy()

        # Compute the noise-drop diff between the pure-OCR word set
        # (captured as ``live_pre_noise_words`` references at the top of
        # this method) and the post-noise word set (the live page right
        # now, before reorg-proper begins). The diff is by ``id()``
        # identity, which is robust to ``refine_bounding_boxes`` having
        # tightened bbox coordinates between the two snapshots — the
        # underlying ``Word`` instances are mutated in place by refine
        # rather than replaced, so a dropped word is unambiguously a word
        # whose ``id`` is no longer reachable from the live page.
        #
        # Each surviving dropped word is serialized via
        # ``Word.to_dict``/``Word.from_dict`` so the captured instance is
        # decoupled from the live tree (which is about to be rewritten by
        # reorg-proper). The text + bbox surface there reflects the OCR
        # engine's output as it entered the noise step — exactly the
        # information the CLI needs to format the "likely noise" warning.
        post_noise_word_ids = {id(w) for w in self.words}
        dropped_live_words = [
            w for w in live_pre_noise_words if id(w) not in post_noise_word_ids
        ]
        self.diagnostic_noise_dropped_words = [
            Word.from_dict(w.to_dict()) for w in dropped_live_words
        ]
        self.diagnostic_noise_dropped_count = len(self.diagnostic_noise_dropped_words)

        # Step D — split mixed-content (caption + body) OCR lines.
        reorganize_page_utils.run_step_d_split_mixed_content(self, debug_sections)

        # Step E — peel page header / footer bands.
        (
            _header_lines,
            _footer_lines,
            body_lines,
            body_words,
            page_header_block,
            page_footer_block,
        ) = reorganize_page_utils.run_step_e_extract_header_footer(self, debug_sections)

        # Step F — vertical row-block grouping.
        row_blocks = reorganize_page_utils.run_step_f_row_blocks(
            self, body_lines, body_words, debug_sections
        )

        if not row_blocks or len(row_blocks.items) == 0:
            logger.debug("No blocks to reorganize")
            if reorganize_page_utils.layout_debug_enabled():
                debug_sections.append(("Step 2", ["No row blocks to reorganize."]))
                reorganize_page_utils.write_layout_debug_report(self, debug_sections)
            reorganize_page_utils.emit_band_only_blocks(
                self, page_header_block, page_footer_block
            )
            if layout is not None:
                from pd_book_tools.ocr import layout_aware_reorg

                layout_aware_reorg.bubble_block_roles_from_layout(self._items)
                layout_aware_reorg.route_sidenote_reading_order(self)
                layout_aware_reorg.associate_captions(
                    self,
                    layout,
                    emit_placeholders=emit_illustration_placeholders,
                )
            return

        for block in row_blocks.items:
            reorganize_page_utils.reorganize_lines(block)

        # Step H — column / floated-figure expansion of each row block.
        expanded_row_blocks, step_h_decisions = reorganize_page_utils.expand_row_blocks(
            self, row_blocks, debug_squeezed_lines
        )
        reorganize_page_utils.emit_step_h_debug(
            self, debug_sections, debug_squeezed_lines, step_h_decisions
        )

        # Step L — classify and paragraphize each expanded block.
        reset_paragraph_blocks = reorganize_page_utils.classify_and_paragraphize_blocks(
            self, expanded_row_blocks
        )

        # Step DC — detect drop caps in the body blocks and stitch them
        # into the next paragraph's first line. The block-cap path catches
        # plain Roman caps the OCR model already recognised; the cursive
        # fallback catches the cases where DocTR mis-read or skipped the
        # oversized serif glyph entirely. Both paths produce the same
        # structural shape: a separate Word at the front of the body line,
        # tagged ``word_components=["drop cap"]``. ``Block.text`` keys on
        # that tag to fuse the cap to the next body Word with the empty
        # string (so cap "S" + body "tudies" renders as "Studies"). See
        # ``pd_book_tools/ocr/dropcap.py``.
        from pd_book_tools.ocr.dropcap import detect_and_stitch_drop_caps

        page_metrics = reorganize_page_utils.compute_page_metrics(self)
        reset_paragraph_blocks = detect_and_stitch_drop_caps(
            reset_paragraph_blocks,
            self.cv2_numpy_page_image,
            page_metrics,
        )

        # Final assembly — weave header/footer back in.
        final_blocks = reorganize_page_utils.assemble_final_blocks(
            page_header_block, reset_paragraph_blocks, page_footer_block
        )

        # Word-preservation safety net. The pipeline must not drop OCR
        # words; if it does, either raise (strict mode for tests/CI) or
        # recover by appending the dropped words as a tagged block + emit a
        # big stderr warning. Re-stamps override_page_sort_order so the
        # recovered block sorts to the very end.
        final_blocks = reorganize_page_utils.reconcile_dropped_words(
            self, pre_reorg_words, final_blocks
        )
        for idx, block in enumerate(final_blocks):
            block.override_page_sort_order = idx

        self.items = final_blocks
        logger.debug(
            "Page Block Count after adding paragraphs:" + str(len(self.paragraphs))
        )
        self.refresh_page_images()

        reorganize_page_utils.emit_step_k_debug(self, debug_sections, final_blocks)
        reorganize_page_utils.emit_step_l_debug(self, debug_sections, final_blocks)
        reorganize_page_utils.emit_step_dropcap_debug(
            self, debug_sections, final_blocks
        )
        if reorganize_page_utils.layout_debug_enabled():
            reorganize_page_utils.write_layout_debug_report(self, debug_sections)

        if layout is not None:
            from pd_book_tools.ocr import layout_aware_reorg

            # Step Layout-3: stamp block_role_labels on each top-level block
            # based on the dominant ``layout:*`` tag inside. Runs *before*
            # caption association so the placeholder illustration / caption
            # blocks emitted by associate_captions don't get re-tagged.
            layout_aware_reorg.bubble_block_roles_from_layout(self._items)

            # Step Layout-3b: route sidenote blocks — left margin emits at
            # the top of the page, right margin at the bottom — so they
            # don't interleave with body lines.
            layout_aware_reorg.route_sidenote_reading_order(self)

            # Step Layout-4: emit placeholder illustration + caption blocks
            # for high-confidence figure / decoration / table regions.
            # ``emit_illustration_placeholders=False`` suppresses the
            # geometry-only placeholder block (useful for plain-text
            # consumers) without dropping caption OCR words.
            layout_aware_reorg.associate_captions(
                self,
                layout,
                emit_placeholders=emit_illustration_placeholders,
            )

    def add_ground_truth(self, text: str):
        update_page_with_ground_truth_text(self, text)
        self.refresh_page_images()

    def remove_ground_truth(self):
        """Remove ground truth text from the page"""
        for item in self.items:
            item.remove_ground_truth()
        if self.unmatched_ground_truth_lines:
            self.unmatched_ground_truth_lines.clear()
        else:
            self.unmatched_ground_truth_lines = []
        self.refresh_page_images()

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> Page:
        """Create OCRPage from dictionary"""
        # Resolve source from either "source" or legacy "page_source" key
        source = data.get("source", data.get("page_source", "ocr"))
        review_raw = data.get("review")
        review = (
            review_raw
            if isinstance(review_raw, ReviewMetadata)
            else (
                ReviewMetadata.from_dict(review_raw) if review_raw is not None else None
            )
        )
        raw_items = data.get("items", [])
        blocks = [
            item if isinstance(item, Block) else Block.from_dict(item)
            for item in raw_items
        ]
        bb_raw = data.get("bounding_box")
        bounding_box = (
            bb_raw
            if isinstance(bb_raw, BoundingBox)
            else (BoundingBox.from_dict(bb_raw) if bb_raw else None)
        )
        return cls(
            blocks=blocks,
            width=data["width"],
            height=data["height"],
            page_index=data["page_index"],
            bounding_box=bounding_box,
            ocr_provenance=data.get("ocr_provenance"),
            image_path=data.get("image_path"),
            name=data.get("name"),
            source=source,
            ocr_failed=data.get("ocr_failed", False),
            provenance_live_ocr=data.get("provenance_live_ocr"),
            provenance_saved_ocr=data.get("provenance_saved_ocr"),
            provenance_saved=data.get("provenance_saved"),
            rotation_applied=data.get("rotation_applied", 0),
            review=review,
        )

    def recompute_bounding_box(self):
        """Recompute the bounding box of the page based on its items.

        ``BoundingBox.union`` is strict about empty input, so we also
        bail out when every item happens to have no bounding box (which
        can happen mid-removal in ``remove_empty_items`` while emptied
        children still occupy the items list).
        """
        if not self.items:
            self.bounding_box = None
            logger.debug("No items in page to recompute bounding box")
            return
        bboxes = [item.bounding_box for item in self.items if item.bounding_box]
        if not bboxes:
            self.bounding_box = None
            return
        self.bounding_box = BoundingBox.union(bboxes)

    def refine_bounding_boxes(self, image: ndarray | None = None, padding_px: int = 0):
        if image is None:
            if hasattr(self, "cv2_numpy_page_image"):
                image = self.cv2_numpy_page_image
            else:
                raise ValueError(
                    "Image not provided and cv2_numpy_page_image is not set."
                )
        for item in self.items:
            item.refine_bounding_boxes(image, padding_px=padding_px)
        self.recompute_bounding_box()

    def generate_doctr_checks(self, output_path: pathlib.Path):
        if self.cv2_numpy_page_image is None:
            raise ValueError(
                "cv2_numpy_page_image is not set. Please set it before calling this method."
            )
        if not self.items:
            logger.info("No items in the page to process.")
            return

        if not output_path.parent.exists():
            raise ValueError(
                "Output path does not exist. Please create the parent directory first."
            )
        output_path.mkdir(parents=True, exist_ok=True)

    def generate_doctr_detection_training_set(
        self: Page,
        output_path: pathlib.Path,
        prefix: str = "",
        word_filter: Callable[[Word], bool] | None = None,
    ) -> None:
        """
        Create a DocTR text detection training or validation set from a page
        image (matched_ocr data) and image bounding boxes.

        Parameters
        ----------
        output_path : pathlib.Path
            Root output directory.  A ``detection/`` subdirectory is created.
        prefix : str
            Prefix for exported image filenames.
        word_filter : callable, optional
            ``(Word) -> bool`` predicate. When provided, only words that
            return ``True`` contribute polygons to the detection labels.

        Result::

            Files:
            ├── detection
              ├── images
                ├──── <prefix>_<page_index>.png (image)
                └──── ...
              ├── labels.json

           Detection labels.json::

            {
                "image_path_1": {
                    'img_dimensions': (x, y),
                    'img_hash': "theimagedumpmyhash",
                    'polygons': [[[x10, y10], [x20, y20], [x30, y30], [x40, y40]], ...]
                }
            }
        """
        self.generate_doctr_checks(output_path)

        # Create the detection directory
        detection_path = pathlib.Path(output_path, "detection")
        detection_path.mkdir(parents=True, exist_ok=True)
        detection_image_path = pathlib.Path(detection_path, "images")
        detection_image_path.mkdir(parents=True, exist_ok=True)

        if self.cv2_numpy_page_image is None:
            raise ValueError(
                "cv2_numpy_page_image is not set. Please set it before calling this method."
            )
        image = cv2_cvtColor(self.cv2_numpy_page_image, cv2_COLOR_BGR2RGB)
        img_height, img_width, _ = image.shape

        detection_labels = {}

        image_name = f"{prefix}_{self.page_index}.png"
        logger.debug("Writing image: " + str(image_name))
        cv2_imwrite(
            str(pathlib.Path(detection_image_path, image_name).resolve()),
            image,
            [int(cv2_IMWRITE_PNG_COMPRESSION), 9],
        )

        # compute sha-256 hash of the image file
        # read the image file back in as bytes
        with open(pathlib.Path(detection_image_path, image_name), "rb") as f:
            image_buffer = f.read()
            image_hash = sha256(image_buffer).hexdigest()

        words = self.words
        if word_filter is not None:
            words = [w for w in words if word_filter(w)]

        detection_labels[image_name] = {
            "img_dimensions": (img_width, img_height),
            "img_hash": image_hash,
            "polygons": [
                word.bounding_box.get_four_point_scaled_polygon_list(
                    img_width, img_height
                )
                for word in words
            ],
        }

        # Read in the JSON file and add the new labels
        try:
            with open(pathlib.Path(detection_path, "labels.json")) as f:
                existing_detection_labels = json_load(f)
        except FileNotFoundError:
            # If the file doesn't exist, create an empty dictionary
            existing_detection_labels = {}

        # remove existing labels that have prefix + page index
        existing_detection_labels = {
            k: v
            for k, v in existing_detection_labels.items()
            if not k.startswith(f"{prefix}_{self.page_index}")
        }

        # Add the new labels to the existing labels
        detection_labels = {**existing_detection_labels, **detection_labels}

        # Write the labels to the JSON file
        with open(pathlib.Path(detection_path, "labels.json"), "w") as f:
            json_dump(detection_labels, f, indent=4, ensure_ascii=False)

    def generate_doctr_recognition_training_set(
        self: Page,
        output_path: pathlib.Path,
        prefix: str = "",
        word_filter: Callable[[Word], bool] | None = None,
        label_formatter: Callable[[Word], Any] | None = None,
    ) -> None:
        """
        Create a text recognition training or validation set from a page
        image (matched_ocr data) and image bounding boxes.

        Parameters
        ----------
        output_path : pathlib.Path
            Root output directory.  A ``recognition/`` subdirectory is created.
        prefix : str
            Prefix for exported image filenames.
        word_filter : callable, optional
            ``(Word) -> bool`` predicate.  When provided, only words that
            return ``True`` are exported as recognition crops.
        label_formatter : callable, optional
            ``(Word) -> Any`` — custom label builder.  When provided, each
            word's label in ``labels.json`` is the return value of
            ``label_formatter(word)`` instead of ``word.ground_truth_text``.
            Words without ``ground_truth_text`` are still skipped unless a
            *label_formatter* is supplied.

        Result::

            Files:
            ├── recognition
              ├── images
                ├──── <prefix>_<page_index>_x1_x2_y1_y2.png
                └──── ...
              ├── labels.json

            Recognition labels.json::

            {
                "image_path_1": "<gt value>",
            }
        """
        self.generate_doctr_checks(output_path)

        # Create the recognition directory
        recognition_path = pathlib.Path(output_path, "recognition")
        recognition_path.mkdir(parents=True, exist_ok=True)
        recognition_image_path = pathlib.Path(recognition_path, "images")
        recognition_image_path.mkdir(parents=True, exist_ok=True)

        if self.cv2_numpy_page_image is None:
            raise ValueError(
                "cv2_numpy_page_image is not set. Please set it before calling this method."
            )
        image = cv2_cvtColor(self.cv2_numpy_page_image, cv2_COLOR_BGR2RGB)
        img_height, img_width, _ = image.shape

        words = self.words
        if word_filter is not None:
            words = [w for w in words if word_filter(w)]

        # Delete any existing images that match the prefix + page index in the output directory
        for file in recognition_image_path.glob(f"{prefix}_{self.page_index}_*"):
            if file.is_file():
                logger.debug("Deleting existing image: " + str(file))
                file.unlink()

        recognition_labels = {}

        for word in words:
            if label_formatter is not None:
                label = label_formatter(word)
            elif word.ground_truth_text:
                label = word.ground_truth_text
            else:
                logger.warning("Skipping word without ground truth text: %s", word.text)
                continue

            bb = word.bounding_box.scale(width=img_width, height=img_height)
            cropped_image = image[bb.minY : bb.maxY, bb.minX : bb.maxX]
            cropped_image_name = f"{prefix}_{self.page_index}_{bb.minX}_{bb.maxX}_{bb.minY}_{bb.maxY}.png"
            logger.debug("Writing image: " + str(cropped_image_name))
            cv2_imwrite(
                str(pathlib.Path(recognition_image_path, cropped_image_name).resolve()),
                cropped_image,
                [int(cv2_IMWRITE_PNG_COMPRESSION), 9],
            )
            recognition_labels[cropped_image_name] = label

        # Read in the JSON file and add the new labels
        try:
            with open(pathlib.Path(recognition_path, "labels.json")) as f:
                existing_labels = json_load(f)
        except FileNotFoundError:
            # If the file doesn't exist, create an empty dictionary
            existing_labels = {}

        # remove existing labels that have prefix + page index
        existing_labels = {
            k: v
            for k, v in existing_labels.items()
            if not k.startswith(f"{prefix}_{self.page_index}_")
        }

        # Add the new labels to the existing labels
        recognition_labels = {**existing_labels, **recognition_labels}

        # Write the updated labels to the JSON file
        with open(pathlib.Path(recognition_path, "labels.json"), "w") as f:
            json_dump(recognition_labels, f, indent=4, ensure_ascii=False)

    def convert_to_training_set(
        self: Page,
        output_path: pathlib.Path,
        prefix: str = "",
        word_filter: Callable[[Word], bool] | None = None,
    ) -> None:
        """
        Create recognition and detection training sets from a page image
        (matched_ocr data) and image bounding boxes.

        Parameters
        ----------
        output_path : pathlib.Path
            Root output directory.
        prefix : str
            Prefix for exported image filenames.
        word_filter : callable, optional
            ``(Word) -> bool`` predicate forwarded to both detection and
            recognition export methods.
        """
        self.generate_doctr_detection_training_set(
            output_path=output_path, prefix=prefix, word_filter=word_filter
        )
        self.generate_doctr_recognition_training_set(
            output_path=output_path, prefix=prefix, word_filter=word_filter
        )

