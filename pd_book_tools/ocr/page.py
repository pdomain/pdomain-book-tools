class BBoxColors(Enum):
    """Enum for different colors used to draw bounding boxes"""

    PAGE = (255, 0, 255)  # Magenta
    BLOCK = (0, 255, 0)  # Green
    PARAGRAPH = (255, 0, 0)  # Red
    LINE = (0, 0, 255)  # Blue
    WORD = (0, 128, 0)  # Dark Green
    GROUND_TRUTH = (0, 255, 255)  # Cyan
    BLACK = (0, 0, 0)  # Black
    DARK_YELLOW = (0, 225, 225)  # Darker Yellow


@dataclass(eq=False)
class Page:
    """Represents a page (single or multiple "blocks") of OCR results.

    R-02 (2026-05-07): converted to a full ``@dataclass`` form. The
    constructor is now generated by the dataclass machinery; the only
    custom ``__init__`` surface is a thin deprecation shim (defined
    after the class body) that translates the renamed legacy kwargs
    ``items=`` → ``blocks=`` and ``cv2_numpy_page_image=`` →
    ``image_array=``. The legacy aliases will be removed in v1.0.

    ``__eq__`` is hand-written rather than dataclass-generated because
    the auto-generated equality would compare ndarray cache fields,
    which raises "ambiguous truth value" — a latent bug fixed by this
    conversion.
    """

    width: int
    height: int
    page_index: int

    # InitVars: data passed in but transformed before storage. Cannot
    # be named ``items`` or ``cv2_numpy_page_image`` because those are
    # @property names on this class — Python's dataclass decorator
    # would resolve the property descriptor as the field default
    # at decoration time and break things.
    blocks: InitVar[Collection | None] = None
    image_array: InitVar[ndarray | None] = None

    bounding_box: BoundingBox | None = None
    page_labels: list[str] | None = None
    unmatched_ground_truth_lines: list | None = None
    "List of Ground Truth Lines and the line they were found on before an OCR match"

    original_ocr_tool_text: str | None = ""
    original_ground_truth_text: str | None = ""
    ocr_provenance: OCRProvenance | dict[str, Any] | None = None

    # Page metadata fields. These were previously assigned only inside
    # the custom ``__init__``; R-02 declared them as proper fields so
    # the dataclass-generated init handles them.
    image_path: pathlib.Path | str | None = None
    name: str | None = None
    source: str = "ocr"
    ocr_failed: bool = False
    provenance_live_ocr: dict[str, Any] | None = None
    provenance_saved_ocr: dict[str, Any] | None = None
    provenance_saved: dict[str, Any] | None = None

    # Rotation OCR applied to the source image before recognition. 0
    # means OCR ran on the page as it sat on disk; 90/180/270 mean the
    # image was rotated clockwise by that many degrees first because
    # the upright pass was below confidence threshold. Pixel and bbox
    # coordinates on this Page index into the rotated frame, not the
    # original. See pd_book_tools.ocr.rotation. Validated in
    # ``__post_init__``.
    rotation_applied: int = 0

    # Optional human-review metadata (Page-scope). See
    # pd_book_tools/ocr/review.py.
    review: ReviewMetadata | None = None

    # Internal state — not part of the public init signature.
    _items: list[Block] = field(
        default_factory=list, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_page_with_bbox: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_blocks_with_bboxes: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_paragraph_with_bboxes: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_line_with_bboxes: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_word_with_bboxes: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_word_with_bboxes_and_ocr_text: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_word_with_bboxes_and_gt_text: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )
    _cv2_numpy_page_image_matched_word_with_colors: ndarray | None = field(
        default=None, init=False, repr=False, compare=False
    )

    # Diagnostic snapshots populated by ``reorganize_page`` when
    # ``capture_diagnostics`` is enabled (default). Each holds a deep
    # copy of the page captured at a specific stage of the pipeline,
    # exposed for CLI / debugging consumption. They are NOT used by
    # any production logic, NOT persisted by ``to_dict`` / ``copy``,
    # and remain ``None`` for any page that has not been reorganized.
    # See ``Page.reorganize_page`` for the capture points and the
    # ``docs/specs/03-reorganize-pipeline.md`` notes.
    diagnostic_pure_ocr: Page | None = field(
        default=None, init=False, repr=False, compare=False
    )
    diagnostic_post_noise_removal: Page | None = field(
        default=None, init=False, repr=False, compare=False
    )
    # Words deleted by the noise-removal steps (Step Layout-2b and
    # Step B2) between the two diagnostic snapshots. Each entry is a
    # deep-copied ``Word`` instance, captured from the live page at
    # the moment of removal. NOT persisted by ``to_dict`` / ``copy``
    # and reset on each ``reorganize_page`` call.
    diagnostic_noise_dropped_words: list[Word] = field(
        default_factory=list, init=False, repr=False, compare=False
    )
    diagnostic_noise_dropped_count: int = field(
        default=0, init=False, repr=False, compare=False
    )

    def __post_init__(
        self,
        blocks: Collection | None,
        image_array: ndarray | None,
    ) -> None:
        if blocks is None:
            raise TypeError(
                "Page() missing required argument: 'blocks' (or the "
                "deprecated alias 'items')."
            )

        # Use the property setter for validation + sort.
        self.items = blocks

        if not self.bounding_box and self.items:
            # Mirror Page.recompute_bounding_box: filter out items whose
            # bounding_box is None before unioning. BoundingBox.union does
            # not tolerate None entries (it accesses .is_normalized on the
            # first element). An empty child block can legitimately have
            # bounding_box=None, so without this guard constructing a Page
            # whose items list contains such a block raises AttributeError.
            bboxes = [item.bounding_box for item in self.items if item.bounding_box]
            self.bounding_box = BoundingBox.union(bboxes) if bboxes else None

        if image_array is not None:
            if not isinstance(image_array, ndarray):
                raise TypeError("image_array must be a numpy ndarray")
            # Use the setter so refresh_page_images() runs.
            self.cv2_numpy_page_image = image_array

        # Coerce: ``None`` stays ``None`` per the legacy contract; an
        # empty/falsy non-None gets normalized to an empty list.
        if not self.unmatched_ground_truth_lines:
            self.unmatched_ground_truth_lines = []

        self.ocr_provenance = OCRProvenance.coerce(self.ocr_provenance)

        if self.rotation_applied not in (0, 90, 180, 270):
            raise ValueError(
                "rotation_applied must be one of 0/90/180/270, got "
                f"{self.rotation_applied}"
            )

    def __eq__(self, other: object) -> bool:
        """Compare two Pages by their persisted, observable state.

        Skips the ndarray image-cache fields (``_cv2_numpy_page_image*``)
        and the diagnostic_* attributes — those would otherwise raise
        "the truth value of an array is ambiguous" via the
        dataclass-generated default.
        """
        if not isinstance(other, Page):
            return NotImplemented
        return self.to_dict() == other.to_dict()

    def __hash__(self) -> int:  # pragma: no cover - lists in to_dict are unhashable
        # Pages are mutable containers; explicit unhashable for clarity.
        raise TypeError("Page is mutable and unhashable")

    @property
    def index(self) -> int:
        """Compatibility alias for ``page_index``."""
        return self.page_index

    @index.setter
    def index(self, value: int) -> None:
        self.page_index = int(value)

    @property
    def page_source(self) -> str:
        """Compatibility alias for ``source``."""
        return self.source

    @page_source.setter
    def page_source(self, value: str) -> None:
        self.source = value

    def _sort_items(self):
        self._items.sort(
            key=lambda item: (
                item.override_page_sort_order
                if item.override_page_sort_order is not None
                else 10**9,
                round(item.bounding_box.top_left.y, 2) if item.bounding_box else 0,
                item.bounding_box.top_left.x if item.bounding_box else 0,
            ),
        )

    @property
    def items(self) -> list[Block]:
        """Returns a copy of the item list in this page"""
        self._sort_items()
        return self._items.copy()

    def add_item(self, item):
        """Add an item to the page"""
        if not isinstance(item, Block):
            raise TypeError("Item must be of type Block")
        self._items.append(item)
        self._sort_items()
        self.recompute_bounding_box()

    def remove_item(self, item):
        """Remove a block from the page"""
        if item in self._items:
            self._items.remove(item)
            self._sort_items()
            self.recompute_bounding_box()
        else:
            raise ValueError("Item not found in page")

    def remove_line_if_exists(self, line):
        """Remove a line from the page if it exists"""
        removed = False

        if line in self._items:
            self.remove_item(line)
            removed = True
        else:
            for block in self._items:
                if block.remove_line_if_exists(line):
                    removed = True
                    break

        if removed:
            logger.debug(f"Line {line.text[0:10]}... removed from page")
        else:
            logger.debug(f"Line {line.text[0:10]}... not found in page")

        return removed

    def remove_empty_items(self):
        """Remove empty child blocks from the page.

        Bulk-removes all empties and recomputes the bounding box once,
        mirroring :meth:`Block.remove_empty_items`. The previous
        per-item ``remove_item`` loop was O(n²) and could trip the
        union-of-empty-list crash mid-loop because partly-purged
        children with ``bounding_box=None`` were still in ``_items``
        when each intermediate ``recompute_bounding_box`` ran.
        """
        if not self._items:
            return
        for item in self._items:
            item.remove_empty_items()
        before = len(self._items)
        self._items = [item for item in self._items if item.items]
        if len(self._items) != before:
            self._sort_items()
            self.recompute_bounding_box()
            logger.debug("Empty blocks removed from page")

    @items.setter
    def items(self, values):
        if not isinstance(values, Collection):
            raise TypeError("items must be a collection")
        for block in values:
            if not isinstance(block, Block):
                raise TypeError("Each item in items must be of type Block")
        self._items = sorted(
            values,
            key=lambda block: (
                block.override_page_sort_order
                if block.override_page_sort_order is not None
                else 10**9,
                round(block.bounding_box.top_left.y, 2) if block.bounding_box else 0,
                block.bounding_box.top_left.x if block.bounding_box else 0,
            ),
        )

