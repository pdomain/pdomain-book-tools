# ---------------------------------------------------------------------------
# Deprecation shim for renamed Page constructor kwargs.
#
# R-02 (2026-05-07) renamed ``items=`` to ``blocks=`` and
# ``cv2_numpy_page_image=`` to ``image_array=`` to clear the
# InitVar / @property name collision that blocked the full
# ``@dataclass`` conversion. The legacy names remain accepted with a
# DeprecationWarning until v1.0.
#
# We replace ``Page.__init__`` with a thin wrapper around the
# dataclass-generated init. Since ``items`` and ``cv2_numpy_page_image``
# are @property names on the class, they cannot themselves be declared
# as InitVar fields without re-introducing the original collision.
# A runtime kwarg-translation wrapper is the smallest possible
# backward-compat surface.
# ---------------------------------------------------------------------------

_dataclass_generated_page_init = Page.__init__


def _page_init_with_deprecation_shim(
    self: Page,
    *args: Any,
    items: Collection | None = None,
    cv2_numpy_page_image: ndarray | None = None,
    **kwargs: Any,
) -> None:
    if items is not None:
        if "blocks" in kwargs and kwargs["blocks"] is not None:
            raise TypeError(
                "Page() got both 'blocks' and the deprecated alias "
                "'items' — pass only 'blocks'."
            )
        warnings.warn(
            "Page(items=...) is deprecated; use Page(blocks=...) "
            "instead. The 'items=' constructor kwarg will be removed "
            "in v1.0. (The read-side 'page.items' property is "
            "unchanged.)",
            DeprecationWarning,
            stacklevel=2,
        )
        kwargs["blocks"] = items

    if cv2_numpy_page_image is not None:
        if "image_array" in kwargs and kwargs["image_array"] is not None:
            raise TypeError(
                "Page() got both 'image_array' and the deprecated "
                "alias 'cv2_numpy_page_image' — pass only "
                "'image_array'."
            )
        warnings.warn(
            "Page(cv2_numpy_page_image=...) is deprecated; use "
            "Page(image_array=...) instead. The "
            "'cv2_numpy_page_image=' constructor kwarg will be "
            "removed in v1.0. (The read-side "
            "'page.cv2_numpy_page_image' property is unchanged.)",
            DeprecationWarning,
            stacklevel=2,
        )
        kwargs["image_array"] = cv2_numpy_page_image

    _dataclass_generated_page_init(self, *args, **kwargs)


